class MorseCode:

    def __init__(self):
        self.morse_code_dict = {
            '.-': 'A', '-.': 'N', '-----': '0',
            '-...': 'B', '---': 'O', '.----': '1',
            '-.-.': 'C', '.--.': 'P', '..---': '2',
            '-..': 'D', '--.-': 'Q', '...--': '3',
            '.': 'E', '.-.': 'R', '....-': '4',
            '..-.': 'F', '...': 'S', '.....': '5',
            '--.': 'G', '-': 'T', '-....': '6',
            '....': 'H', '..-': 'U', '--...': '7',
            '..': 'I', '...-': 'V', '---..': '8',
            '.---': 'J', '.--': 'W', '----.': '9',
            '-.-': 'K', '-..-': 'X', '.-.-.-': '.',
            '.-..': 'L', '-.--': 'Y', '--..--': ',',
            '--': 'M', '--..': 'Z', '..--..': '?',
            '-..-.': '/', '.-.-.': '+', '-...-': '=',
            '-.--.': '(', '-.--.-': ')', '-.-.--': '!',
            '.-...': '&', '-.-.-.': ';', '---...': ':',
            '-....-': '-', '..--.-': '_', '.-..-.': '"',
            '...-..-': '$', '.--.-.': '@'
        }

    def decrypt(self, ciphertext):
        plaintext = ''.join(self.morse_code_dict.get(i, ' ') for i in ciphertext.split())
        return plaintext
